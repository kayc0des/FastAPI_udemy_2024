''' Python Lists '''

# create a list of 5 animals called zoo
zoo = ['lion', 'monkey', 'crocodile', 'tiger', 'black mamba']

# delete the animal at the 3rd Index
zoo.pop(2)

# append a new animal to the end of the list
zoo.append('antelope')

# Delete the animal at the begining of the list
zoo.pop(0)

# print all the animals
print(zoo)

# print only the first three
print(zoo[:3])